// ==================== HELPER FUNCTIONS ====================
export function getTemplatesByCategory(category: EventType): Template[] {
  return templates.filter((t) => t.category === category);
}

export function getTemplateById(id: string): Template | undefined {
  return templates.find((t) => t.id === id);
}

export function getFreeTemplates(): Template[] {
  return templates.filter((t) => !t.isPremium);
}

export function getPremiumTemplates(): Template[] {
  return templates.filter((t) => t.isPremium);
}

export function getPopularTemplates(limit: number = 10): Template[] {
  return [...templates].sort((a, b) => b.popularity - a.popularity).slice(0, limit);
}

export function getTopRatedTemplates(limit: number = 10): Template[] {
  return [...templates].sort((a, b) => b.rating - a.rating).slice(0, limit);
}

export function searchTemplates(query: string): Template[] {
  const lowerQuery = query.toLowerCase();
  return templates.filter(
    (t) =>
      t.name.toLowerCase().includes(lowerQuery) ||
      t.description.toLowerCase().includes(lowerQuery) ||
      t.tags.some((tag) => tag.toLowerCase().includes(lowerQuery))
  );
}

export function getCategoryCount(): Record<EventType, number> {
  const counts = {} as Record<EventType, number>;
  for (const template of templates) {
    counts[template.category] = (counts[template.category] || 0) + 1;
  }
  return counts;
}
